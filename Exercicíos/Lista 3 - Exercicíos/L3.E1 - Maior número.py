'''
1 - Faça um Programa que peça dois números e imprima o maior deles.
'''

num1 = int(input('Digite o um número: '))
num2 = int(input('Digite o outro número: '))

if num1 > num2:
    print(f'O maior número foi {num1}')
else:
    print(f'O maior número foi {num2}')
