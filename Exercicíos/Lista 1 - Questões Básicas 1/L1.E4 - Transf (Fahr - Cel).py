'''
4. Escreva um programa que leia uma temperatura em graus Fahrenheit,
transforme-a em graus Celsius e exiba o resultado.
'''

fahrenheit = float(input("Digite  valor em Fahrenheit: "))

celsuis = float(fahrenheit - 32) / 1.8

print(f"{fahrenheit} Fahrenheit = {celsuis} Celsuis")
